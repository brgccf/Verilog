Assign --> sintetiza um bloco combinacional
always --> bloco sequencial 
utiliza o trigger @() que executa oque estive dentro do código toda vez que a condiçao dentro de
( ) for verdadeira
se o always tiver um (*) como argumento do trigger, torna-se combinacional
Circuitos sequenciais --> possuem memoria (ex latches ou flip flops)
circuitos combinacionais --> nao possuem memoria (ex portas logicas)

	